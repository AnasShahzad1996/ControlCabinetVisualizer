    public SimpleGui(int width, int height)
    {
        _width = width; _height = height;
        InitQuadShader();
        InitTextShader();
        _fontTex = CreateFontTexture();
    }

    public void Resize(int w, int h) { _width = w; _height = h; }

    public void AddToggleButton(string label, float x, float y, float w, float h,
                                Func<bool> getState, Action toggle)
        => _buttons.Add(new Button(label, x, y, w, h, toggle));

    public void HandleClick(float mx, float my)
    {
        foreach (var b in _buttons)
            if (mx >= b.X && mx <= b.X + b.W && my >= b.Y && my <= b.Y + b.H)
                b.OnClick();
    }

    public void Render(Func<string, bool> getState)
    {
        GL.Disable(EnableCap.DepthTest);
        GL.Enable(EnableCap.Blend);
        GL.BlendFunc(BlendingFactor.SrcAlpha, BlendingFactor.OneMinusSrcAlpha);

        var proj = Matrix4.CreateOrthographicOffCenter(0, _width, _height, 0, -1, 1);

        // Background quads
        GL.UseProgram(_quadShader);
        GL.UniformMatrix4(GL.GetUniformLocation(_quadShader, "proj"), false, ref proj);
        foreach (var b in _buttons)
        {
            bool on = getState(b.Label);
            DrawQuad(b.X, b.Y, b.W, b.H,
                on ? new Vector4(0.15f, 0.50f, 0.15f, 0.92f)
                   : new Vector4(0.50f, 0.10f, 0.10f, 0.92f));
        }

        // Text
        GL.UseProgram(_textShader);
        GL.UniformMatrix4(GL.GetUniformLocation(_textShader, "proj"), false, ref proj);
        GL.Uniform1(GL.GetUniformLocation(_textShader, "fontTex"), 0);
        GL.ActiveTexture(TextureUnit.Texture0);
        GL.BindTexture(TextureTarget.Texture2D, _fontTex);

        foreach (var b in _buttons)
        {
            string label = b.Label.Length > 22 ? b.Label[..22] : b.Label;
            float charW = 8f, charH = 8f, scale = 1.5f;
            float totalW = label.Length * charW * scale;
            float startX = b.X + (b.W - totalW) / 2f;
            float startY = b.Y + (b.H - charH * scale) / 2f;

            for (int i = 0; i < label.Length; i++)
            {
                int c = Math.Clamp(label[i] - 0x20, 0, 95);
                float u0 = c / 96f;
                float u1 = (c + 1) / 96f;
                float x = startX + i * charW * scale;
                DrawGlyph(x, startY, charW * scale, charH * scale, u0, u1);
            }
        }

        GL.Disable(EnableCap.Blend);
        GL.Enable(EnableCap.DepthTest);
    }

    // ── GL helpers ───────────────────────────────────────────────────────────

    int CreateFontTexture()
    {
        // Build a 96x8 RGBA texture — one column per ASCII char
        int numChars = 96;
        var pixels = new byte[numChars * 8 * 8 * 4]; // width=numChars*8, height=8, RGBA

        for (int ch = 0; ch < numChars; ch++)
        {
            for (int row = 0; row < 8; row++)
            {
                byte bits = Font8x8[ch * 8 + row];
                for (int col = 0; col < 8; col++)
                {
                    bool set = (bits & (1 << (7 - col))) != 0;
                    int px = (ch * 8 + col) + row * (numChars * 8);
                    pixels[px * 4 + 0] = 255;
                    pixels[px * 4 + 1] = 255;
                    pixels[px * 4 + 2] = 255;
                    pixels[px * 4 + 3] = set ? (byte)255 : (byte)0;
                }
            }
        }

        int id = GL.GenTexture();
        GL.BindTexture(TextureTarget.Texture2D, id);
        GL.TexImage2D(TextureTarget.Texture2D, 0, PixelInternalFormat.Rgba,
                      numChars * 8, 8, 0,
                      PixelFormat.Rgba, PixelType.UnsignedByte, pixels);
        GL.TexParameter(TextureTarget.Texture2D, TextureParameterName.TextureMinFilter, (int)TextureMinFilter.Nearest);
        GL.TexParameter(TextureTarget.Texture2D, TextureParameterName.TextureMagFilter, (int)TextureMagFilter.Nearest);
        return id;
    }

    void DrawQuad(float x, float y, float w, float h, Vector4 color)
    {
        float[] v = { x, y, x+w, y, x+w, y+h, x, y, x+w, y+h, x, y+h };
        GL.BindVertexArray(_vao);
        GL.BindBuffer(BufferTarget.ArrayBuffer, _vbo);
        GL.BufferData(BufferTarget.ArrayBuffer, v.Length * 4, v, BufferUsageHint.DynamicDraw);
        GL.Uniform4(GL.GetUniformLocation(_quadShader, "color"), color);
        GL.DrawArrays(PrimitiveType.Triangles, 0, 6);
    }

void DrawGlyph(float x, float y, float w, float h, float u0, float u1)
{
    float[] v = {
        x,   y,   u1, 0,
        x+w, y,   u0, 0,
        x+w, y+h, u0, 1,
        x,   y,   u1, 0,
        x+w, y+h, u0, 1,
        x,   y+h, u1, 1
    };
    GL.BindVertexArray(_tVao);
    GL.BindBuffer(BufferTarget.ArrayBuffer, _tVbo);
    GL.BufferData(BufferTarget.ArrayBuffer, v.Length * 4, v, BufferUsageHint.DynamicDraw);
    GL.DrawArrays(PrimitiveType.Triangles, 0, 6);
}
    void InitQuadShader()
    {
        _vao = GL.GenVertexArray(); GL.BindVertexArray(_vao);
        _vbo = GL.GenBuffer();      GL.BindBuffer(BufferTarget.ArrayBuffer, _vbo);
        GL.EnableVertexAttribArray(0);
        GL.VertexAttribPointer(0, 2, VertexAttribPointerType.Float, false, 8, 0);
        _quadShader = Compile(
            @"#version 330 core
              layout(location=0) in vec2 pos;
              uniform mat4 proj;
              void main(){ gl_Position=proj*vec4(pos,0,1); }",
            @"#version 330 core
              uniform vec4 color; out vec4 FragColor;
              void main(){ FragColor=color; }");
    }

    void InitTextShader()
    {
        _tVao = GL.GenVertexArray(); GL.BindVertexArray(_tVao);
        _tVbo = GL.GenBuffer();      GL.BindBuffer(BufferTarget.ArrayBuffer, _tVbo);
        GL.EnableVertexAttribArray(0);
        GL.VertexAttribPointer(0, 2, VertexAttribPointerType.Float, false, 16, 0);
        GL.EnableVertexAttribArray(1);
        GL.VertexAttribPointer(1, 2, VertexAttribPointerType.Float, false, 16, 8);
        _textShader = Compile(
            @"#version 330 core
              layout(location=0) in vec2 pos;
              layout(location=1) in vec2 uv;
              uniform mat4 proj;
              out vec2 vUV;
              void main(){ vUV=uv; gl_Position=proj*vec4(pos,0,1); }",
            @"#version 330 core
              in vec2 vUV; uniform sampler2D fontTex; out vec4 FragColor;
              void main(){ FragColor=texture(fontTex,vUV); }");
    }

    static int Compile(string vert, string frag)
    {
        int v = GL.CreateShader(ShaderType.VertexShader);
        GL.ShaderSource(v, vert); GL.CompileShader(v);
        int f = GL.CreateShader(ShaderType.FragmentShader);
        GL.ShaderSource(f, frag); GL.CompileShader(f);
        int p = GL.CreateProgram();
        GL.AttachShader(p, v); GL.AttachShader(p, f);
        GL.LinkProgram(p);
        GL.DeleteShader(v); GL.DeleteShader(f);
        return p;
    }
